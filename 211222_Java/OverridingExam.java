package Java20211222;

public class OverridingExam {

	public static void main(String[] args) {
/*

		오버라이딩 overriding
		- 조상 클래스로부터 상속 받은 매서드의 내용을 변경하여 동일한 이름으로 자식 클래스에 만드는 것
		
		1. 부모의 매서드 이름과 동일한 이름을 사용한다.
		2. 부모의 매서드에 있는 매개 변수와 같은 매개 변수 형과 개수를 사용한다.
		3. 부모의 매서드에 있는 반환 타입과도 동일하여야 한다.
		
		- 오버 로딩과 오버 라이딩은 단어가 비슷해서 혼동을 일으키기 쉬움
		  오버로딩 : 기존에 없는 새로운 매서드를 정의하는 것 (new)
		  오버라이딩 : 상속 받은 매서드의 내용을 변경하는 것 (change, modify)
		  		예제)
				class Parent {
					void parentMethod() {
					}
				}
				
				class Child extends Parent {
					void parentMethod(){}		->오버라이딩
					void parentMethod(int i){}	->오버로딩
					
					void childMethod(){}
					void childMethod(int i){}	-> 오버로딩하여 변경
					void childMethod(){} -> 에러 발생 : childMethod를 중복으로 정의함
				}
		
		*super : 생성자에서 this를 사용하듯, 상속에서는 super를 사용하여 부모 클래스를 가리킴.		
		
		
*/
	}

}
